public class ColorCube {
    public int minR;
    public int minG;
    public int minB;
    public int maxR;
    public int maxG;
    public int maxB;

    public ColorCube(int minB, int maxB, int minG, int maxG, int minR, int maxR) {
        this.minB = minB;
        this.maxB = maxB;
        this.minG = minG;
        this.maxG = maxG;
        this.minR = minR;
        this.maxR = maxR;
    }
}
